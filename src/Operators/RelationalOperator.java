package Operators;

public class RelationalOperator {

	public static void main(String[] args) {
	
		int A = 10;	
		int B = 20;
		int C = 40;
		int D = 40;
		
		
		System.out.println(A==B);
		System.out.println(A!=B);
		System.out.println(A>B);
		System.out.println(A<B);
		System.out.println(C>=D);
		System.out.println(C<=D);
		
	}

}
